package curso.estruturasDeControle;

public class While_1 {
    public static void main(String[] args) {
        int count = 1;

        while (count <= 10) {
            System.out.println("Bom dia ");
            count++;
        }

        count = 1;

        while (count <= 20){
            int i = count;
            System.out.println(i);
            count++;
        }
    }
}
